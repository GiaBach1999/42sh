#include "libft.h"

void		ft_initialize_str(char **s1, char **s2, char **s3,
			char **s4)
{
	if (s1)
		*s1 = NULL;
	if (s2)
		*s2 = NULL;
	if (s3)
		*s3 = NULL;
	if (s4)
		*s4 = NULL;
}
