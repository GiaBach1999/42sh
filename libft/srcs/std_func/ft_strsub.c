#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*res;
	size_t	i;

	if (s)
	{
		if (!(res = ft_strnew(len)))
			return (NULL);
		i = 0;
		while (len)
		{
			res[i] = s[start + i];
			i++;
			len--;
		}
	}
	else
		res = ft_strnew(0);
	return (res);
}
